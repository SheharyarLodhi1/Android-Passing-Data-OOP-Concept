public class Factorial {
    private int num;
    private int fact;

    public Factorial(int num, int fact) {
        this.num = num;
        this.fact = fact;

    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public int getFact() {
        return fact;
    }

    public void setFact(int fact) {
        this.fact = fact;
    }
}
